"""
1904 01 타일 DP

1  2  3   4
1 11 111 1111
  00 001 0011
     100 1001
         1100
         0000

         
1 1/1 11/1 111/1
  00  00/1 001/1
      1/00 100/1
           11/00
           00/00

1개 전 + 1
2개 전 + 00

틀렸습니다 => N 이 1인 경우 고려 못함
"""

N = int(input())

#DP[N][2]
DP = [[0, 0] for i in range(N+1)]
DP[0][0] = 1
DP[0][1] = 0
DP[1][0] = 1
DP[1][1] = 1

for i in range(2,N):
    DP[i][0] += DP[i-1][0] + DP[i-1][1]
    DP[i][1] += DP[i-2][0] + DP[i-2][1]
    DP[i][0] %= 15746
    DP[i][1] %= 15746

print(sum(DP[N-1])%15746)
